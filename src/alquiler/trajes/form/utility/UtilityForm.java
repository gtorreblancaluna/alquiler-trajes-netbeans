    private void init () {
        
        disableForm();
        
        try {
            Long plusDays = 
                    Long.valueOf(PropertySystemUtil.get(PropertyConstant.PLUS_DAYS_TO_EVENTS_IN_MAIN_FORM));            
            txtPlusDaysToEventsInMainForm.setText(String.valueOf(plusDays));
            
            
            Integer minutesToRequestNewSession = 
                    Integer.valueOf(PropertySystemUtil.get(PropertyConstant.MINUTES_TO_REQUEST_NEW_SESSION));
            txtMinutesToRequestNewSession.setText(String.valueOf(minutesToRequestNewSession));
            
            Boolean calculateReturnDate = 
                    Boolean.valueOf(PropertySystemUtil.get(PropertyConstant.CALCULATE_RETURN_DATE));
            checkCalculateReturnDate.setSelected(calculateReturnDate);           
            
            
         } catch(IOException e){
            logger.log(Level.SEVERE,e.getMessage(),e);           
            JOptionPane.showMessageDialog(this, e, ApplicationConstants.MESSAGE_UNEXPECTED_ERROR,
                    JOptionPane.ERROR_MESSAGE);
        } 
        
    }
    
    private void disableForm () {
        txtMinutesToRequestNewSession.setEnabled(false);
        txtPlusDaysToEventsInMainForm.setEnabled(false);
        checkCalculateReturnDate.setEnabled(false);
        jbtnSave.setEnabled(false);
    }
    
    private void enableForm () {
        txtMinutesToRequestNewSession.setEnabled(true);
        txtPlusDaysToEventsInMainForm.setEnabled(true);
        checkCalculateReturnDate.setEnabled(true);
        jbtnSave.setEnabled(true);
    }

