    private void jbtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSaveActionPerformed
       try {
            
           Long plusDays = Long.valueOf(txtPlusDaysToEventsInMainForm.getText().trim());
           Integer minutesToRequestNewSession = Integer.valueOf(txtMinutesToRequestNewSession.getText().trim());
           Boolean calculateReturnDate = checkCalculateReturnDate.isSelected();
           
           PropertySystemUtil
                   .save(PropertyConstant.MINUTES_TO_REQUEST_NEW_SESSION.getKey(),String.valueOf(minutesToRequestNewSession));
           PropertySystemUtil
                   .save(PropertyConstant.PLUS_DAYS_TO_EVENTS_IN_MAIN_FORM.getKey(),String.valueOf(plusDays));
           PropertySystemUtil
                   .save(PropertyConstant.CALCULATE_RETURN_DATE.getKey(),String.valueOf(calculateReturnDate)); 
           
           this.disableForm();
           
       } catch (NumberFormatException numberFormatException) {
           JOptionPane.showMessageDialog(
                   this, ApplicationConstants.MESSAGE_TITLE_ERROR,
                   ApplicationConstants.MESSAGE_NUMBER_FORMAT_ERROR,
                   JOptionPane.ERROR_MESSAGE);
       } catch (IOException ioe) {
           JOptionPane.showMessageDialog(
                   this, ApplicationConstants.MESSAGE_TITLE_ERROR,
                   ioe.getMessage(),
                   JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jbtnSaveActionPerformed

